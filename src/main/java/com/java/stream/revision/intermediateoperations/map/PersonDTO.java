package com.java.stream.revision.intermediateoperations.map;

public class PersonDTO {

    private int id;
    private String name;


    public PersonDTO(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
